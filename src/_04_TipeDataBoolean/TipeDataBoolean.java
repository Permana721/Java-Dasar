package _04_TipeDataBoolean;
/**
 * Tipe data boolean  adalah tipe data yang memiliki 2 nilai, yaitu benar dan salah
 * Tipe data boolean di Java direpresentasikan dengan kata kunci boolean
 * Nilai benar direpresentasikan dengan kata kunci true
 * Nilai salah direpresentasikan dengan kata kunci false
 * Default value untuk boolean adalah false
 * Tipe data boolean biasanya digunakan untuk kondisi, seperti pada percabangan if, switch, dan perulangan while, for, do-while
 * Tipe data boolean juga digunakan untuk menyimpan hasil dari operasi logika, seperti AND, OR, dan NOT
 */
public class TipeDataBoolean {
    public static void main(String[] args) {
        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);
    }
}