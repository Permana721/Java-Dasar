package _03_TipeDataChar;
/**
 * Data Character (huruf) di Java direpresentasikan oleh tipe char.
 * Untuk membuat data char di Java, kita bisa menggunakan tanda ‘ (petik satu) di awal dan di akhir karakter
 * Tipe data char hanya bisa menyimpan 1 karakter saja, seperti huruf, angka, simbol, atau spasi
 * Tipe data char memiliki ukuran 2 byte (16 bit) dan dapat menyimpan karakter
 */
public class TipeDataChar {
    public static void main(String[] args) {
        char p = 'P';
        char e = 'E';
        char r = 'R';

        System.out.println("P");
        System.out.println("E");
        System.out.println("R");
    }
}